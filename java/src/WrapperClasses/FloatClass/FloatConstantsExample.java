package WrapperClasses.FloatClass;

public class FloatConstantsExample {
    public static void main(String[] args) {
        // Спеціальне значення плюс нескінченність.
        System.out.println("POSITIVE_INFINITY: " + Float.POSITIVE_INFINITY);

        // Спеціальне значення мінус нескінченність.
        System.out.println("NEGATIVE_INFINITY: " + Float.NEGATIVE_INFINITY);

        // Спеціальне значення "не число" (NaN).
        System.out.println("NaN: " + Float.NaN);

        // Найбільше допустиме значення типу float.
        System.out.println("MAX_VALUE: " + Float.MAX_VALUE);

        // Найменше допустиме ненульове значення типу float.
        System.out.println("MIN_VALUE: " + Float.MIN_VALUE);

        // Максимальний допустимий експонент типу float.
        System.out.println("MAX_EXPONENT: " + Float.MAX_EXPONENT);

        // Мінімальний допустимий експонент типу float.
        System.out.println("MIN_EXPONENT: " + Float.MIN_EXPONENT);

        // Кількість бітів, що використовуються для зберігання значення типу float.
        System.out.println("SIZE: " + Float.SIZE + " bits");

        // Кількість байтів, що використовуються для зберігання значення типу float.
        System.out.println("BYTES: " + Float.BYTES + " bytes");

        // Мінімальне ненульове нормалізоване значення типу float.
        System.out.println("MIN_NORMAL: " + Float.MIN_NORMAL);

        // Об'єкт класу Class, який представляє тип float.
        System.out.println("TYPE: " + Float.TYPE);

        // Застаріла константа, яка визначає кількість десяткових цифр точності для типу float.
        System.out.println("PRECISION: " + Float.PRECISION);


    }
}
