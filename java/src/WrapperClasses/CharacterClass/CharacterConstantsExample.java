package WrapperClasses.CharacterClass;

public class CharacterConstantsExample {
    public static void main(String[] args) {
        // Кількість байтів, необхідних для представлення одного символу
        System.out.println("BYTES: " + Character.BYTES);

        // Символ, який є комбінувальним пробілом
        System.out.println("COMBINING_SPACING_MARK: " + Character.COMBINING_SPACING_MARK);

        // Символ, який є роздільником з'єднання
        System.out.println("CONNECTOR_PUNCTUATION: " + Character.CONNECTOR_PUNCTUATION);

        // Контрольний символ
        System.out.println("CONTROL: " + Character.CONTROL);

        // Символ, який є символом валюти
        System.out.println("CURRENCY_SYMBOL: " + Character.CURRENCY_SYMBOL);

        // Символ, який є тире
        System.out.println("DASH_PUNCTUATION: " + Character.DASH_PUNCTUATION);

        // Символ, який є десятковою цифрою
        System.out.println("DECIMAL_DIGIT_NUMBER: " + Character.DECIMAL_DIGIT_NUMBER);

        // Напрямок арабських чисел
        System.out.println("DIRECTIONALITY_ARABIC_NUMBER: " + Character.DIRECTIONALITY_ARABIC_NUMBER);

        // Напрямок нейтральних символів на межі
        System.out.println("DIRECTIONALITY_BOUNDARY_NEUTRAL: " + Character.DIRECTIONALITY_BOUNDARY_NEUTRAL);

        // Напрямок роздільників загальних чисел
        System.out.println("DIRECTIONALITY_COMMON_NUMBER_SEPARATOR: " + Character.DIRECTIONALITY_COMMON_NUMBER_SEPARATOR);

        // Напрямок європейських чисел
        System.out.println("DIRECTIONALITY_EUROPEAN_NUMBER: " + Character.DIRECTIONALITY_EUROPEAN_NUMBER);

        // Напрямок роздільників європейських чисел
        System.out.println("DIRECTIONALITY_EUROPEAN_NUMBER_SEPARATOR: " + Character.DIRECTIONALITY_EUROPEAN_NUMBER_SEPARATOR);

        // Напрямок термінаторів європейських чисел
        System.out.println("DIRECTIONALITY_EUROPEAN_NUMBER_TERMINATOR: " + Character.DIRECTIONALITY_EUROPEAN_NUMBER_TERMINATOR);

        // Напрямок символів зліва направо
        System.out.println("DIRECTIONALITY_LEFT_TO_RIGHT: " + Character.DIRECTIONALITY_LEFT_TO_RIGHT);

        // Напрямок вбудованих символів зліва направо
        System.out.println("DIRECTIONALITY_LEFT_TO_RIGHT_EMBEDDING: " + Character.DIRECTIONALITY_LEFT_TO_RIGHT_EMBEDDING);

        // Напрямок перевизначених символів зліва направо
        System.out.println("DIRECTIONALITY_LEFT_TO_RIGHT_OVERRIDE: " + Character.DIRECTIONALITY_LEFT_TO_RIGHT_OVERRIDE);

        // Символ, який є знаком без проміжків
        System.out.println("DIRECTIONALITY_NONSPACING_MARK: " + Character.DIRECTIONALITY_NONSPACING_MARK);

        // Напрямок інших нейтральних символів
        System.out.println("DIRECTIONALITY_OTHER_NEUTRALS: " + Character.DIRECTIONALITY_OTHER_NEUTRALS);

        // Напрямок роздільників абзаців
        System.out.println("DIRECTIONALITY_PARAGRAPH_SEPARATOR: " + Character.DIRECTIONALITY_PARAGRAPH_SEPARATOR);

        // Напрямок форматованих поправок
        System.out.println("DIRECTIONALITY_POP_DIRECTIONAL_FORMAT: " + Character.DIRECTIONALITY_POP_DIRECTIONAL_FORMAT);

        // Напрямок символів справа наліво
        System.out.println("DIRECTIONALITY_RIGHT_TO_LEFT: " + Character.DIRECTIONALITY_RIGHT_TO_LEFT);

        // Напрямок арабських символів справа наліво
        System.out.println("DIRECTIONALITY_RIGHT_TO_LEFT_ARABIC: " + Character.DIRECTIONALITY_RIGHT_TO_LEFT_ARABIC);

        // Напрямок вбудованих символів справа наліво
        System.out.println("DIRECTIONALITY_RIGHT_TO_LEFT_EMBEDDING: " + Character.DIRECTIONALITY_RIGHT_TO_LEFT_EMBEDDING);

        // Напрямок перевизначених символів справа наліво
        System.out.println("DIRECTIONALITY_RIGHT_TO_LEFT_OVERRIDE: " + Character.DIRECTIONALITY_RIGHT_TO_LEFT_OVERRIDE);

        // Напрямок роздільників сегментів
        System.out.println("DIRECTIONALITY_SEGMENT_SEPARATOR: " + Character.DIRECTIONALITY_SEGMENT_SEPARATOR);

        // Напрямок не визначений
        System.out.println("DIRECTIONALITY_UNDEFINED: " + Character.DIRECTIONALITY_UNDEFINED);

        // Напрямок символів пробілів
        System.out.println("DIRECTIONALITY_WHITESPACE: " + Character.DIRECTIONALITY_WHITESPACE);

        // Символ, який є вкладеним маркером
        System.out.println("ENCLOSING_MARK: " + Character.ENCLOSING_MARK);

        // Символ, який є кінцевим роздільником
        System.out.println("END_PUNCTUATION: " + Character.END_PUNCTUATION);

        // Символ, який є заключним лапками
        System.out.println("FINAL_QUOTE_PUNCTUATION: " + Character.FINAL_QUOTE_PUNCTUATION);

        // Символ із функцією форматування
        System.out.println("FORMAT: " + Character.FORMAT);

        // Символ, який є початковим лапками
        System.out.println("INITIAL_QUOTE_PUNCTUATION: " + Character.INITIAL_QUOTE_PUNCTUATION);

        // Символ, який є літерою-цифрою
        System.out.println("LETTER_NUMBER: " + Character.LETTER_NUMBER);

        // Символ, який є роздільником рядка
        System.out.println("LINE_SEPARATOR: " + Character.LINE_SEPARATOR);

        // Символ, який є малим регістрованим літерою
        System.out.println("LOWERCASE_LETTER: " + Character.LOWERCASE_LETTER);

        // Математичний символ
        System.out.println("MATH_SYMBOL: " + Character.MATH_SYMBOL);

        // Максимальна кодова точка Unicode
        System.out.println("MAX_CODE_POINT: " + Character.MAX_CODE_POINT);

        // Максимальна значення у діапазоні високих підрядних
        System.out.println("MAX_HIGH_SURROGATE: " + Character.MAX_HIGH_SURROGATE);

        // Максимальна значення у діапазоні низьких підрядних
        System.out.println("MAX_LOW_SURROGATE: " + Character.MAX_LOW_SURROGATE);

        // Максимальна основа, що використовується для перетворень
        System.out.println("MAX_RADIX: " + Character.MAX_RADIX);

        // Максимальна кодова точка підрядних
        System.out.println("MAX_SURROGATE: " + Character.MAX_SURROGATE);

        // Максимальне значення типу `char`
        System.out.println("MAX_VALUE: " + Character.MAX_VALUE);

        // Мінімальна кодова точка Unicode
        System.out.println("MIN_CODE_POINT: " + Character.MIN_CODE_POINT);

        // Мінімальна значення у діапазоні високих підрядних
        System.out.println("MIN_HIGH_SURROGATE: " + Character.MIN_HIGH_SURROGATE);

        // Мінімальна значення у діапазоні низьких підрядних
        System.out.println("MIN_LOW_SURROGATE: " + Character.MIN_LOW_SURROGATE);

        // Мінімальна основа, що використовується для перетворень
        System.out.println("MIN_RADIX: " + Character.MIN_RADIX);

        // Мінімальна допоміжна кодова точка Unicode
        System.out.println("MIN_SUPPLEMENTARY_CODE_POINT: " + Character.MIN_SUPPLEMENTARY_CODE_POINT);

        // Мінімальне значення підрядних
        System.out.println("MIN_SURROGATE: " + Character.MIN_SURROGATE);

        // Мінімальне значення типу `char`
        System.out.println("MIN_VALUE: " + Character.MIN_VALUE);

        // Символ, який є літерою-модифікатором
        System.out.println("MODIFIER_LETTER: " + Character.MODIFIER_LETTER);

        // Символ, який є модифікаторним символом
        System.out.println("MODIFIER_SYMBOL: " + Character.MODIFIER_SYMBOL);

        // Символ, який є символом без проміжків
        System.out.println("NON_SPACING_MARK: " + Character.NON_SPACING_MARK);

        // Інший літерний символ
        System.out.println("OTHER_LETTER: " + Character.OTHER_LETTER);

        // Інша числова категорія
        System.out.println("OTHER_NUMBER: " + Character.OTHER_NUMBER);

        // Інша пунктуаційна категорія
        System.out.println("OTHER_PUNCTUATION: " + Character.OTHER_PUNCTUATION);

        // Інший символ
        System.out.println("OTHER_SYMBOL: " + Character.OTHER_SYMBOL);

        // Роздільник абзаців
        System.out.println("PARAGRAPH_SEPARATOR: " + Character.PARAGRAPH_SEPARATOR);

        // Символ, який є для приватного використання
        System.out.println("PRIVATE_USE: " + Character.PRIVATE_USE);

        // Розмір типу `char` у байтах
        System.out.println("SIZE: " + Character.SIZE);

        // Символ, який є роздільником пробілу
        System.out.println("SPACE_SEPARATOR: " + Character.SPACE_SEPARATOR);

        // Символ, який є початковою пунктуацією
        System.out.println("START_PUNCTUATION: " + Character.START_PUNCTUATION);

        // Символ, який є підрядним символом
        System.out.println("SURROGATE: " + Character.SURROGATE);

        // Символ, який є літерою у великому регістрі
        System.out.println("TITLECASE_LETTER: " + Character.TITLECASE_LETTER);

        // Категорія символу
        System.out.println("TYPE: " + Character.TYPE);

        // Категорія невизначеного символу
        System.out.println("UNASSIGNED: " + Character.UNASSIGNED);

        // Символ, який є літерою у малому регістрі
        System.out.println("UPPERCASE_LETTER: " + Character.UPPERCASE_LETTER);

    }
}
