package WrapperClasses.BooleanClass;

public class BooleanConstantsExample {
    public static void main(String[] args) {
        // Використання константи TRUE
        boolean trueValue = Boolean.TRUE;
        System.out.println("TRUE: " + trueValue);

        // Використання константи FALSE
        boolean falseValue = Boolean.FALSE;
        System.out.println("FALSE: " + falseValue);
    }

}
