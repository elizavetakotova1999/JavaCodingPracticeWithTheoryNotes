package PrimitiveDataType.simple;

public class BooleanExample {
    public static void main(String[] args) {
        boolean isTrue = true; // Оголошення та ініціалізація змінної типу boolean
        System.out.println("Is True: " + isTrue);

        boolean isFalse = false;
        System.out.println("Is False: " + isFalse);

        boolean isGreaterThan = 10 > 5; // Логічна операція (більше ніж)
        System.out.println("Is Greater Than: " + isGreaterThan);
    }
}
