package PrimitiveDataType.simple;

public class CharExample {
    public static void main(String[] args) {
        char grade = 'A'; // Оголошення та ініціалізація символьної змінної
        System.out.println("Grade: " + grade);

        char letter = 65; // Символьне значення також може бути представлене числовим кодом Unicode
        System.out.println("Letter: " + letter);

        char newLine = '\n'; // Спеціальний символ нового рядка
        System.out.println("New Line: " + newLine);
    }
}
