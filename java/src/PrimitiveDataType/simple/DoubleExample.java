package PrimitiveDataType.simple;

public class DoubleExample {
    public static void main(String[] args) {
        double pi = 3.14159; // Оголошення та ініціалізація змінної типу double
        System.out.println("Pi: " + pi);

        double radius = 5.0;
        double area = Math.PI * radius * radius; // Використання математичних операцій та константи з класу Math
        System.out.println("Area: " + area);
    }
}